    private void colocarIconos(){
        int alto = 50;
        int ancho = 50; 
        
        jButtonTurnos.setIcon(prepararIcono("iconTurnos.png",alto,ancho)); // asignamos al boton el icono
        jButtonBusquedas.setIcon(prepararIcono("icon_buscar.png",alto,ancho)); // asignamos al boton el icono
        jButtonClientes.setIcon(prepararIcono("iconClientes.png",alto,ancho)); // asignamos al boton el icono
        jButtonProfesionales.setIcon(prepararIcono("iconProfesionales.png", alto, ancho)); // asignamos al boton el icono
        jButtonEmpleados.setIcon(prepararIcono("iconEmpleados.png",alto,ancho));
        jButtonInstalaciones.setIcon(prepararIcono("iconInstalaciones.png",alto,ancho));
        jButtonConsultorios.setIcon(prepararIcono("iconConsultorios.png",alto,ancho));
        jButtonTienda.setIcon(prepararIcono("iconTienda.png",alto,ancho));
        jButtonSalir.setIcon(prepararIcono("icon_salir.png",alto,ancho));
        
        ImageIcon icono = new ImageIcon();
        icono = prepararIcono("IconVista.png",20,20);
        this.setIconImage(icono.getImage());
    }
    
    private ImageIcon prepararIcono(String nombre,int alto,int ancho){ // Aplicacion que prepara los Iconos para ser puestos donde se quiera se pasa el nombre y el tamaño que quieres que tenga
        // Obtén la ruta relativa a la ubicación de la clase Controlador eso es la carpeta SRC del proyecto ese seria la carpeta de inicio
        ClassLoader directorio = getClass().getClassLoader();
        URL imagen = directorio.getResource("Icons/" + nombre); // Creamos la ruta al recurso (iconos)
        // Crea un ImageIcon utilizando la URL de la imagen
        ImageIcon icono = new ImageIcon(imagen); // creamos la Imagen Icono para asignarsela al contenerdor
        // Redimensionar el icono pasandolo a imagen con el nuevo tamaño y luego convirtiendolo en icono XD
        Image imagenRedimensionada = icono.getImage().getScaledInstance(alto, ancho, Image.SCALE_SMOOTH);
        icono = new ImageIcon(imagenRedimensionada);
        return icono;
    }
